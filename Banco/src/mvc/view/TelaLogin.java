    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String login = txtUsuario.getText();
        int senha = Integer.parseInt(new String( txtSenha.getPassword()));
        
        if(usuarioLogado.logar(login, senha) != null){
            JOptionPane.showMessageDialog(rootPane, "Bem vindo, " + usuarioLogado.getLogado().getNome() + "!");
            dispose();
            //Chama classe adm
            if(usuarioLogado.verificaAdm()){
                JOptionPane.showMessageDialog(rootPane, "Bem vindo ADM!");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Bem vindo Cliente!");
                
            }
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Usu√°rio e/ou Senha incorretos!");
        }
        
    }//GEN-LAST:event_btnEntrarActionPerformed

