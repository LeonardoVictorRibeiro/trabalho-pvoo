    public void atualizarTabela(){
        
        DefaultTableModel dtmFundos = (DefaultTableModel)jTFundos.getModel();
        dtmFundos.setNumRows(0);
        
        
        List<Fundo> fundos = new FundoDAO().listar();
        
        for (Fundo fundo : fundos) {
            dtmFundos.addRow( 
                    new Object[]{
                        fundo.getId(), fundo.getNome()
                    }
            
            );
        }
        
        txtId.setText("");
        txtNome.setText("");
        txtQta.setText("");
        
         
    }

